-- THIS SUGGESTS THAT THERE IS A PROBLEM/GLITCH IN THE POS OR THE EMPLOYEE BEHIND IT DOES NOT RECORD THE LOCATION OPTION
-- TAKEAWAY AND IN-STORE ARE HEAD TO HEAD MEANING THE CAFE IS BUILT TO SERVE BOTH IN-STORE AND TAKEAWAYS

